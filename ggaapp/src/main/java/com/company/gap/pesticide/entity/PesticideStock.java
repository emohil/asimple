package com.company.gap.pesticide.entity;


public class PesticideStock {
	private int stock_id;
	private int stock_nameid;
	private int stock_sizeid;
	private int stock_batchid;
	private int stock_producerid;
	private int stock_kindid;
	private double stock_quantity;
	private int stock_status;
	public int getStock_id() {
		return stock_id;
	}
	public void setStock_id(int stock_id) {
		this.stock_id = stock_id;
	}
	public int getStock_nameid() {
		return stock_nameid;
	}
	public void setStock_nameid(int stock_nameid) {
		this.stock_nameid = stock_nameid;
	}
	public int getStock_sizeid() {
		return stock_sizeid;
	}
	public void setStock_sizeid(int stock_sizeid) {
		this.stock_sizeid = stock_sizeid;
	}
	public int getStock_batchid() {
		return stock_batchid;
	}
	public void setStock_batchid(int stock_batchid) {
		this.stock_batchid = stock_batchid;
	}
	public int getStock_producerid() {
		return stock_producerid;
	}
	public void setStock_producerid(int stock_producerid) {
		this.stock_producerid = stock_producerid;
	}
	public int getStock_kindid() {
		return stock_kindid;
	}
	public void setStock_kindid(int stock_kindid) {
		this.stock_kindid = stock_kindid;
	}
	public double getStock_quantity() {
		return stock_quantity;
	}
	public void setStock_quantity(double stock_quantity) {
		this.stock_quantity = stock_quantity;
	}
	public int getStock_status() {
		return stock_status;
	}
	public void setStock_status(int stock_status) {
		this.stock_status = stock_status;
	}
	
	
}